"""Tests for the AM sensitivity estimator beyond the pure helpers.

Three concerns, all offline (the AM model is a local subprocess, no network):

- **Profile prep** — ``AmModelPredictor`` clips/clamps atmospheric profiles
  deterministically; asserted without running AM.
- **Real-AM smoke** — ``AmModelPredictor.run`` and ``VlaSeasonalPredictor.run``
  actually invoke amwrap (~0.5 s each) to guard the radiative-transfer
  integration.
- **Estimator assembly** — ``VlaSensitivityEstimator`` orchestration. The
  clear-sky path runs real AM on a truncated input; the branchy cloud/ensemble
  MultiIndex assembly is covered with ``_run_am_model`` monkeypatched (running
  it for real would be one AM model per timestep x quantile).

Input weather comes from ``tests/fixtures/sensitivity/om_{surface,pressure}.parquet``
(regenerated by ``tests/scripts/refresh_sensitivity_fixtures.py``); the cloud
path reuses the Herbie fixture.
"""

from pathlib import Path
from types import SimpleNamespace
from unittest.mock import patch

import numpy as np
import pandas as pd
import pytest
import xarray as xr

from site_forecast.query import herbie_maps as hm
from site_forecast.query.herbie_maps import HerbieQuery
from site_forecast.sensitivity import (
    AmModelPredictor,
    VlaSeasonalPredictor,
    VlaSensitivityEstimator,
)

from schemas.queries import OpenMeteoVlaSchema, OpenMeteoVlaPressureSchema


TESTS_DIR = Path(__file__).parents[1]
SENSITIVITY_DIR = TESTS_DIR / "fixtures" / "sensitivity"
HERBIE_FIXTURE = TESTS_DIR / "fixtures" / "dataframes" / "herbie_tcolw_tiny.nc"


@pytest.fixture(scope="module")
def om_surface() -> pd.DataFrame:
    return pd.read_parquet(SENSITIVITY_DIR / "om_surface.parquet")


@pytest.fixture(scope="module")
def om_pressure() -> pd.DataFrame:
    return pd.read_parquet(SENSITIVITY_DIR / "om_pressure.parquet")


@pytest.fixture(scope="module")
def shared_times(om_surface, om_pressure) -> pd.DatetimeIndex:
    """Hourly timestamps present in both frames (the estimator intersects them)."""
    pres_dates = om_pressure.index.get_level_values("date").unique()
    return pres_dates.intersection(om_surface.index)


@pytest.fixture(scope="module")
def herbie_tcolw_ds():
    """A real reduced Herbie ``tcolw`` dataset (with ``tcolw_q`` at 10/20 km),
    built by replaying the Herbie fixture through the actual query pipeline."""
    with patch.object(
        hm,
        "get_tcolw",
        lambda **kwargs: xr.load_dataset(HERBIE_FIXTURE),
    ):
        return HerbieQuery(query_type="tcolw").ds


