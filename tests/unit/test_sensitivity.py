# Layer 4 — derived sensitivity columns (known answer)
# ---------------------------------------------------------------------------


def test_add_derived_columns_known_answer():
    est = object.__new__(VlaSensitivityEstimator)  # skip __init__ (no AM run)
    idx = pd.MultiIndex.from_tuples(
        [(pd.Timestamp("2025-01-01"), "L"), (pd.Timestamp("2025-01-01"), "S")],
        names=["date", "band"],
    )
    df = pd.DataFrame(
        {
            "brightness_temperature": [10.0, 20.0],
            "opacity": [0.1, 0.2],
            "transmittance": [0.9, 0.8],
        },
        index=idx,
    )
    baseline = pd.DataFrame(
        {
            "brightness_temperature": [5.0, 5.0],
            "opacity": [0.05, 0.05],
            "transmittance": [0.95, 0.95],
        },
        index=pd.Index(["L", "S"], name="band"),
    )

    out = est._add_derived_columns(df, baseline)

    rt = VlaSensitivityEstimator.receiver_temperatures["L"]
    tsys, tsys_cl = 10.0 + rt, 5.0 + rt
    assert out.loc[idx[0], "system_temperature"] == pytest.approx(tsys)
    assert out.loc[idx[0], "system_temperature_cl"] == pytest.approx(tsys_cl)
    expected = (0.95 / 0.9) * (tsys / tsys_cl)
    assert out.loc[idx[0], "eff_sensitivity"] == pytest.approx(expected)
    assert out.loc[idx[0], "eff_time"] == pytest.approx(expected**2)


