def test_input_fixtures_satisfy_open_meteo_schemas(om_surface, om_pressure):
    # Parquet stores the datetime index at coarser (ms) resolution than the
    # live query's [s]; normalise before validating against the [s]-pinned
    # schemas. Structural columns/dtypes are what we're guarding here.
    surf = om_surface.copy()
    surf.index = surf.index.as_unit("s")
    pres = om_pressure.copy()
    pres.index = pres.index.set_levels(pres.index.levels[0].as_unit("s"), level="date")
    OpenMeteoVlaSchema.validate(surf)
    OpenMeteoVlaPressureSchema.validate(pres)


# ---------------------------------------------------------------------------
# Layer 2 — AmModelPredictor profile prep (no AM run)
# ---------------------------------------------------------------------------


def test_am_predictor_profile_prep_from_frames(om_surface, om_pressure, shared_times):
    t = shared_times[0]
    predictor = AmModelPredictor.from_frames(
        om_surface.loc[t], om_pressure.xs(t, level="date")
    )

    # Profiles are aligned and pressure is monotically descending.
    n = len(predictor.pres)
    assert len({n, len(predictor.temp), len(predictor.relh), len(predictor.mixr)}) == 1
    assert predictor.pres[0] > predictor.pres[-1]

    # Reference PWV is positive and pwv_scale is the target/model ratio.
    assert predictor.model_pwv.to("mm").value > 0
    assert np.isclose(
        predictor.pwv_scale, (predictor.pwv / predictor.model_pwv).to("").value
    )

    # Stratospheric water is clamped up to the floor; humidity floored to eps.
    strato = predictor.pres < predictor.strato_pressure_limit
    if strato.any():
        floor = predictor.strato_mixing_ratio.to("").value
        assert np.all(predictor.mixr[strato].to("").value >= floor)
    assert np.all(
        predictor.relh.to("").value >= predictor.relative_humidity_epsilon.value
    )


