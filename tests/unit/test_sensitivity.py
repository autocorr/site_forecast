def test_estimator_skips_when_weather_unavailable(shared_times):
    """A failed surface query short-circuits without raising; both outputs are
    None and okay is False."""
    surf = _fake_query(df=None, okay=False, forecast_time=shared_times[0])
    pres = _fake_query(df=None, okay=True)

    est = VlaSensitivityEstimator(surf, pres, None, n_workers=1)

    assert est.clear_df is None
    assert est.cloud_df is None
    assert est.okay is False


def test_save_data_writes_parquet(monkeypatch, tmp_path):
    est = object.__new__(VlaSensitivityEstimator)
    est.clear_df = pd.DataFrame(
        {"eff_sensitivity": [1.0, 1.1]},
        index=pd.MultiIndex.from_tuples(
            [(pd.Timestamp("2025-01-01"), "L"), (pd.Timestamp("2025-01-01"), "S")],
            names=["date", "band"],
        ),
    )
    est.cloud_df = None
    monkeypatch.setattr(
        VlaSensitivityEstimator, "forecast_dir", property(lambda self: tmp_path)
    )

    est.save_data()

    assert (tmp_path / "vla_sensitivity.parquet").exists()
