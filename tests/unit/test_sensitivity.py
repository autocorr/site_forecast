# Layer 6 — estimator clear-sky path end-to-end (real AM, truncated input)
# ---------------------------------------------------------------------------


def _fake_query(df=None, okay=True, forecast_time=None, ds=None) -> SimpleNamespace:
    return SimpleNamespace(df=df, okay=okay, forecast_time=forecast_time, ds=ds)


def test_estimator_clear_path_end_to_end(om_surface, om_pressure, shared_times):
    """Full construct + compute over two shared timesteps, running real AM
    single-threaded. Only the clear-sky path is populated (no cloud/ensemble),
    so `okay` (which requires both frames) is False."""
    times = shared_times[:2]
    surf = _fake_query(df=om_surface.loc[times], okay=True, forecast_time=times[0])
    pres = _fake_query(
        df=om_pressure[om_pressure.index.get_level_values("date").isin(times)],
        okay=True,
    )

    est = VlaSensitivityEstimator(surf, pres, None, n_workers=1)

    assert est.clear_df is not None
    assert est.clear_df.index.names == ["date", "band"]
    assert "eff_sensitivity" in est.clear_df.columns
    assert est.cloud_df is None
    assert est.okay is False


def test_estimator_clear_path_uses_worker_pool(om_surface, om_pressure, shared_times):
    """Same clear-sky path with ``n_workers=2``, so ``_run_series`` takes the real
    ``multiprocessing.Pool`` branch: ``_worker_init`` runs in each forked worker
    (building/cleaning its own AM cache dir via ``_get_worker_cache_path``), the
    AM args pickle across the process boundary, real AM runs there, and results
    aggregate back. Guards the pool path production uses at ``n_workers=20``."""
    times = shared_times[:2]
    surf = _fake_query(df=om_surface.loc[times], okay=True, forecast_time=times[0])
    pres = _fake_query(
        df=om_pressure[om_pressure.index.get_level_values("date").isin(times)],
        okay=True,
    )

    est = VlaSensitivityEstimator(surf, pres, None, n_workers=2)

    assert est.clear_df is not None
    assert est.clear_df.index.names == ["date", "band"]
    assert "eff_sensitivity" in est.clear_df.columns
    # Every timestep round-tripped through the pool: n_timesteps x n_bands rows.
    n_bands = len(VlaSensitivityEstimator.band_frequencies)
    assert len(est.clear_df) == len(times) * n_bands
    assert set(est.clear_df.index.get_level_values("date")) == set(times)


